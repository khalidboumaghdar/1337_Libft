#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*x;

	i = 0;
	if (!s)
		return ;
	x = s;
	while (i < n)
	{
		x[i] = '\0';
		i++;
	}
}
