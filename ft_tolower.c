#include <stdio.h>
#include <ctype.h>


int ft_tolower(int c)
{
     if (c >= 65 && c <= 90 || c >= 97 && c <= 122)
    {
        c += 32 ; 
    }
    return (c);
}
int main(void)
{
    int x; 

    x = ft_tolower('E');
    printf("%d \n" , x);
}
